-- ============================================================
-- QUERY 65
-- TITLE: Churn Rate by Charge Group
-- DESCRIPTION: Creates charge groups and calculates churn rate.
-- ============================================================

WITH charge_groups AS (
    SELECT
        *,
        CASE
            WHEN monthlycharges < 30 THEN 'Low Charge'
            WHEN monthlycharges BETWEEN 30 AND 70 THEN 'Medium Charge'
            ELSE 'High Charge'
        END AS charge_group
    FROM customer_churn
)
SELECT
    charge_group,
    COUNT(*) AS total_customers,
    COUNT(*) FILTER (WHERE churn = 'Yes') AS churned_customers,
    ROUND(
        COUNT(*) FILTER (WHERE churn = 'Yes') * 100.0
        / NULLIF(COUNT(*), 0),
        2
    ) AS churn_rate_percentage
FROM charge_groups
GROUP BY charge_group
ORDER BY churn_rate_percentage DESC;


