-- ============================================================
-- QUERY 97
-- TITLE: Customers Generating First 80 Percent of Revenue
-- DESCRIPTION: Identifies customers whose cumulative revenue contribution
--              reaches the first 80% of total revenue.
-- ============================================================

WITH customer_revenue AS (
    SELECT
        customerid,
        totalcharges,
        SUM(totalcharges) OVER (
            ORDER BY totalcharges DESC
            ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
        ) AS cumulative_revenue,
        SUM(totalcharges) OVER () AS total_revenue
    FROM customer_churn
)
SELECT
    customerid,
    totalcharges,
    ROUND(
        (cumulative_revenue * 100.0 / NULLIF(total_revenue, 0))::numeric,
        2
    ) AS cumulative_revenue_percentage
FROM customer_revenue
WHERE cumulative_revenue <= total_revenue * 0.80
ORDER BY totalcharges DESC;


