    customerid,
    tenure,
    monthlycharges,
    churn,
    CASE
        WHEN churn = 'Yes'
             AND tenure < 12
             AND monthlycharges > 70
            THEN 'High Risk'
        WHEN churn = 'Yes'
             OR tenure < 12
            THEN 'Medium Risk'
        ELSE 'Low Risk'
    END AS risk_category
FROM customer_churn
ORDER BY 
    CASE
        WHEN churn = 'Yes'
             AND tenure < 12
             AND monthlycharges > 70 THEN 1
        WHEN churn = 'Yes'
             OR tenure < 12 THEN 2
        ELSE 3
    END;


